// ============================================================================
// uart_game.h
// ----------------------------------------------------------------------------
// Учебная ветка проекта "Робот в лабиринте", в которой движение робота
// выполняется только по текстовым пакетам, имитирующим обмен по UART.
//
// Сценарий работы:
// 1. Пользователь загружает лабиринт из файла.
// 2. Поле рисуется в исходном состоянии.
// 3. Программа ждёт строку пакета.
// 4. Команда разбирается, робот выполняет шаги по одному, после каждого шага
//    поле перерисовывается.
// 5. После каждого действия программа печатает строку-ответ, похожую на UART.
// 6. При ошибке движение останавливается, до подтверждения CMD_ACK новые
//    движения не принимаются.
// ============================================================================

void runUartTrainingSession(
    int N,
    int M,
    const int field[MAX_N][MAX_M],
    Pos start,
    const Pos exits[MAX_EXITS],
    int exitCount,
    const MovementSettings& move,
    const char* scenarioPath
);
